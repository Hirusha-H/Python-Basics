# ===== Datatype: Integer ===== #
# Type: <class 'int'>
# Example:,

my_int = 12345
print(type(my_int)) # This will print type of my_int

# Addition
int_1 = 1
int_2 = 2
print(int_1+int_2) # Output: 3

# Subtraction
sub_int1 = 10
sub_int2 = 5
print(sub_int1-sub_int2) # Output: 5



